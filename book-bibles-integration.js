import { generateBibleFromTitle, GENRE_TEMPLATES } from './book-bibles-generator.js';

// ==================== IMPORT BOOK DATA ====================
// This will be populated with books from books-database.js

export const BOOKS_WITH_COMPLETE_BIBLES = {};

/**
 * Initialize all book bibles
 * Call this on server startup to build complete Bible library for all 212 books
 */
export const initializeAllBookBibles = (booksDatabase) => {
  console.log(`📚 Initializing bibles for ${booksDatabase.length} books...`);
  
  let manuallyCuratedCount = 0;
  let autoGeneratedCount = 0;
  
  for (const book of booksDatabase) {
    const { id, title, genre, description } = book;
    
    // Check if we already have a manually curated Bible for this book
    if (MANUALLY_CURATED_BIBLES[id]) {
      BOOKS_WITH_COMPLETE_BIBLES[id] = MANUALLY_CURATED_BIBLES[id];
      manuallyCuratedCount++;
    } else {
      // Auto-generate Bible from title + genre
      BOOKS_WITH_COMPLETE_BIBLES[id] = generateBibleFromTitle(
        id, 
        title, 
        genre, 
        description
      );
      autoGeneratedCount++;
    }
  }
  
  console.log(`✅ Bible Initialization Complete:`);
  console.log(`   📖 Manually Curated: ${manuallyCuratedCount}`);
  console.log(`   🤖 Auto-Generated: ${autoGeneratedCount}`);
  console.log(`   📚 Total: ${manuallyCuratedCount + autoGeneratedCount}`);
  
  return BOOKS_WITH_COMPLETE_BIBLES;
};

/**
 * Get a complete Bible for any book
 * Returns manual Bible if exists, otherwise auto-generated
 */
export const getBookBible = (bookId) => {
  if (!BOOKS_WITH_COMPLETE_BIBLES[bookId]) {
    console.warn(`⚠️ Bible not found for ${bookId}. Returning template.`);
    return {
      title: bookId.replace(/-/g, ' ').toUpperCase(),
      genre: 'Unknown',
      status: 'PENDING_GENERATION'
    };
  }
  
  return BOOKS_WITH_COMPLETE_BIBLES[bookId];
};

/**
 * Get Bible status for all books
 */
export const getBibleStatus = () => {
  const total = Object.keys(BOOKS_WITH_COMPLETE_BIBLES).length;
  const manuallyCurated = Object.keys(MANUALLY_CURATED_BIBLES).length;
  const autoGenerated = total - manuallyCurated;
  
  return {
    totalBooks: total,
    manuallyCuratedBibles: manuallyCurated,
    autoGeneratedBibles: autoGenerated,
    completionPercentage: ((total / 212) * 100).toFixed(1) + '%',
    status: 'Ready for infinite story generation'
  };
};

