import express from 'express';
import cors from 'cors';
import { 
  initializeBibleSystem, 
  getBookBible,
  getBibleStatus,
  getBookIdList 
} from './bible-system-integration.js';
import { generateStoryFromBible } from './bible-generator.js';
import booksDatabase from './books-database.js';

const app = express();
const Port = process.env.PORT || 3000;

app.use(cors());
app.use(express.json());

// ==================== INITIALIZATION ====================

let BIBLES_READY = false;

app.listen(Port, async () => {
  console.log(`\n🚀 Xavier OS Server Starting on Port ${Port}\n`);
  
  // Initialize Bible System on startup
  console.log(`📚 Initializing Bible System...`);
  try {
    await initializeBibleSystem(booksDatabase);
    BIBLES_READY = true;
    const status = getBibleStatus();
    console.log(`✅ Bible System Ready:`);
    console.log(`   📖 Manually Curated: ${status.manuallyCurated}`);
    console.log(`   🤖 Auto-Generated: ${status.autoGenerated}`);
    console.log(`   📚 Total: ${status.total}`);
    console.log(`   Daily Capacity: ${status.capacity.totalStoriesPerDay} stories\n`);
  } catch (error) {
    console.error(`❌ Failed to initialize Bible System:`, error);
  }
});

// ==================== VERIFICATION ROUTES ====================

/**
 * Health check - verify server is running
 */
app.get('/api/health', (req, res) => {
  res.json({ 
    status: 'online',
    biblesReady: BIBLES_READY,
    timestamp: new Date()
  });
});

/**
 * Bible status - check system initialization
 */
app.get('/api/bible-status', (req, res) => {
  if (!BIBLES_READY) {
    return res.status(503).json({ error: 'Bible system not initialized' });
  }
  
  res.json(getBibleStatus());
});

/**
 * List all books with bibles
 */
app.get('/api/books-list', (req, res) => {
  if (!BIBLES_READY) {
    return res.status(503).json({ error: 'Bible system not initialized' });
  }
  
  const bookIds = getBookIdList();
  res.json({ 
    totalBooks: bookIds.length,
    books: bookIds
  });
});

/**
 * Get Bible for specific book
 * Usage: /api/bible/my-vampire-system
 */
app.get('/api/bible/:bookId', (req, res) => {
  if (!BIBLES_READY) {
    return res.status(503).json({ error: 'Bible system not initialized' });
  }
  
  const { bookId } = req.params;
  const bible = getBookBible(bookId);
  
  if (!bible) {
    return res.status(404).json({ error: `No bible found for ${bookId}` });
  }
  
  res.json(bible);
});

// ==================== STORY GENERATION ROUTES ====================

/**
 * MAIN ROUTE: Generate infinite stories
 * 
 * Usage: GET /api/generate?bookId=my-vampire-system&variation=1
 * Returns: Completely unique story every time
 */
app.get('/api/generate', async (req, res) => {
  if (!BIBLES_READY) {
    return res.status(503).json({ 
      error: 'Bible system not initialized',
      retry: 'Please wait for server to initialize bibles'
    });
  }
  
  const { bookId, variation = 1, context = '' } = req.query;
  
  // Validation
  if (!bookId) {
    return res.status(400).json({ error: 'Missing required parameter: bookId' });
  }
  
  try {
    // Get the Bible for this book
    const bible = getBookBible(bookId);
    
    if (!bible) {
      return res.status(404).json({ 
        error: `Book not found: ${bookId}`,
        available: getBookIdList().slice(0, 5) // Show first 5 examples
      });
    }
    
    console.log(`📖 Generating story for: ${bible.title} (variation ${variation})`);
    
    // Generate story using Bible
    const story = await generateStoryFromBible(
      bookId,
      {
        variation: parseInt(variation),
        context,
        userAgent: req.get('user-agent')
      },
      null  // Use default AI service or fallback
    );
    
    if (!story) {
      return res.status(500).json({ 
        error: 'Failed to generate story',
        bookId
      });
    }
    
    // Return story with metadata
    res.json({
      success: true,
      bookId,
      title: bible.title,
      genre: bible.genre,
      variation: parseInt(variation),
      story: story,
      bibleStatus: bible.status,
      timestamp: new Date().toISOString(),
      metadata: {
        storyLength: story.split(' ').length,
        paragraphs: (story.match(/\n\n/g) || []).length + 1,
        estimatedReadTime: `${Math.ceil(story.split(' ').length / 200)} min`
      }
    });
    
  } catch (error) {
    console.error(`❌ Story generation error:`, error);
    res.status(500).json({
      error: 'Story generation failed',
      message: error.message,
      bookId
    });
  }
});

/**
 * Generate multiple story variations
 * 
 * Usage: POST /api/generate-batch
 * Body: { bookId: "my-vampire-system", count: 5 }
 * Returns: Array of 5 different stories
 */
app.post('/api/generate-batch', async (req, res) => {
  if (!BIBLES_READY) {
    return res.status(503).json({ error: 'Bible system not initialized' });
  }
  
  const { bookId, count = 3 } = req.body;
  
  if (!bookId) {
    return res.status(400).json({ error: 'Missing parameter: bookId' });
  }
  
  if (count > 20) {
    return res.status(400).json({ error: 'Max 20 stories per request' });
  }
  
  try {
    const bible = getBookBible(bookId);
    if (!bible) {
      return res.status(404).json({ error: `Book not found: ${bookId}` });
    }
    
    console.log(`📚 Batch generating ${count} stories for: ${bible.title}`);
    
    const stories = [];
    for (let i = 0; i < count; i++) {
      const story = await generateStoryFromBible(
        bookId,
        { variation: i + 1 },
        null
      );
      stories.push(story);
      console.log(`   ✅ Generated story ${i + 1}/${count}`);
    }
    
    res.json({
      success: true,
      bookId,
      title: bible.title,
      count: stories.length,
      stories: stories,
      timestamp: new Date().toISOString()
    });
    
  } catch (error) {
    console.error(`❌ Batch generation error:`, error);
    res.status(500).json({
      error: 'Batch generation failed',
      message: error.message
    });
  }
});

/**
 * Schedule daily story generation
 * 
 * Usage: POST /api/generate-daily-schedule
 * Generates X stories for Y random books, saves to cache
 */
app.post('/api/generate-daily-schedule', async (req, res) => {
  if (!BIBLES_READY) {
    return res.status(503).json({ error: 'Bible system not initialized' });
  }
  
  const { storiesPerBook = 5, selectedBooks = null } = req.body;
  const bookIds = selectedBooks || getBookIdList().slice(0, 10); // Default: first 10 books
  
  try {
    console.log(`\n📅 Starting Daily Story Generation Schedule`);
    console.log(`   Books: ${bookIds.length}`);
    console.log(`   Stories per book: ${storiesPerBook}`);
    console.log(`   Total stories: ${bookIds.length * storiesPerBook}`);
    
    const results = [];
    let totalGenerated = 0;
    
    for (const bookId of bookIds) {
      const bible = getBookBible(bookId);
      if (!bible) continue;
      
      for (let i = 0; i < storiesPerBook; i++) {
        try {
          const story = await generateStoryFromBible(
            bookId,
            { variation: i + 1 },
            null
          );
          
          results.push({
            bookId,
            title: bible.title,
            variation: i + 1,
            length: story.split(' ').length,
            generated: new Date()
          });
          
          totalGenerated++;
          
          // Progress update every 5 stories
          if (totalGenerated % 5 === 0) {
            console.log(`   📖 Generated ${totalGenerated} stories...`);
          }
        } catch (error) {
          console.error(`   ❌ Failed to generate for ${bookId} variation ${i + 1}`);
        }
      }
    }
    
    console.log(`✅ Daily generation complete: ${totalGenerated} stories\n`);
    
    res.json({
      success: true,
      totalGenerated,
      results: results.slice(0, 10), // Return first 10 for brevity
      totalResults: results.length,
      timestamp: new Date().toISOString()
    });
    
  } catch (error) {
    console.error(`❌ Schedule generation error:`, error);
    res.status(500).json({
      error: 'Schedule generation failed',
      message: error.message
    });
  }
});

